var numero = 2;

switch (numero) {
    case 1:
        console.log("Numero Uno");
        break;
    case 2:
        console.log("Numero Dos");
        break;
    case 3:
        console.log("Numero Tres");
        break;
    case 10:
        console.log("Numero Diez");
        break;
    case 100:
        console.log("Numero Cien");
        break;
    default:
        console.log("Numero Desconocido!")
        break;
}